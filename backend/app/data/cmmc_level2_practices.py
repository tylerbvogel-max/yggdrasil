def get_cmmc_practices() -> list[dict]:
    """Return the full CMMC Level 2 practice catalog."""
    return CMMC_PRACTICES


def get_cmmc_summary() -> dict:
    """Return summary statistics for CMMC Level 2 compliance posture."""
    total = len(CMMC_PRACTICES)
    counts: dict[str, int] = {}
    family_counts: dict[str, dict] = {}

    for p in CMMC_PRACTICES:
        status = p["status"]
        counts[status] = counts.get(status, 0) + 1

        fam = p["family"]
        if fam not in family_counts:
            family_counts[fam] = {"name": p["family_name"], "total": 0}
        family_counts[fam]["total"] = family_counts[fam].get("total", 0) + 1
        family_counts[fam][status] = family_counts[fam].get(status, 0) + 1

    families = [{"id": fam_id, **fam_data} for fam_id, fam_data in sorted(family_counts.items())]

    return {
        "framework": "CMMC Level 2",
        "standard": "NIST SP 800-171 Rev 2",
        "total_practices": total,
        "status_counts": counts,
        "families": families,
        "note": "NIST 800-171r2 withdrawn May 2024; CMMC 2.0 Level 2 currently maps to Rev 2. DoD may update to Rev 3.",
    }
